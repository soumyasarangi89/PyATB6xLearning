def display_info(name, role):
    print(f"Name: {name}, Role: {role}")


# Displaying information using keyword arguments
display_info(role="Admin", name="Alice")
display_info(name="Bob", role="User")
display_info(role="Moderator", name="Charlie")
display_info(name="Soumya", role="Guest")
display_info(role="Member", name="David")
display_info(name="Eve", role="Contributor")
display_info(role="Subscriber", name="Frank")
display_info(name="Grace", role="Editor")


