for i in range(0, 10):  # 0 to 9, times -> 10
    print(i)
    if i == 5:
        break
    # print(i)




# |i | Condition |  O/P |
# |0 |  0 == 5 -> False |  O/P = 0
# |1 |  1 ==5 -> False |  O/P = 1
# |2 |  2 ==5 -> False |  O/P = 2
# |3 |  3 ==5 -> False |  O/P = 3
# |4 |  4 ==5 -> False |  O/P = 4
# |5 |  5 ==5 -> True |  O/P = 5 and then BREAK out of For loop